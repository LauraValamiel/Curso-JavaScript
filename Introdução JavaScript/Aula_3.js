//Operadores Aritmeticos: soma, subtração, divisão, multiplicação, potenciação, resto

const primeiroNumero = 100
const segundoNumero = 25
const soma = primeiroNumero + segundoNumero
const subtracao = primeiroNumero - segundoNumero
const divisao = primeiroNumero / segundoNumero
const multiplicacao = primeiroNumero * segundoNumero

console.log('Primeiro número: ', primeiroNumero)
console.log('Segundo número: ', segundoNumero)
console.log('Resultado da soma: ', soma)
console.log('Resultado da subtração: ', subtracao)
console.log('Resultado da divisão: ', divisao)
console.log('Resultado da multiplicação: ', multiplicacao)


